
public class Ingredient {

	int capacity;
	int durability;
	int flavor;
	int texture;
	int calories;
	
	
	public Ingredient(int capacity, int durability, int flavor, int texture,
			int calories) {
		super();
		this.capacity = capacity;
		this.durability = durability;
		this.flavor = flavor;
		this.texture = texture;
		this.calories = calories;
	}
	
	
}
